package sec01.exam03;

public class IfElseIfElseExample2 {

	public static void main(String[] args) {
		int score = 69;
		
		if(score>=90) {
			System.out.println(" 점수가 90~100 점입니다 ");
		} else if (score>=80) {
			System.out.println(" 점수가 80~90 점입니다 ");
			
		} else if (score>=70) {
			System.out.println("점수가 70~80점입니다.");
			
		} else {
			System.out.println("종료합니다");
			
		}
		
		
		
		}
		

	}


