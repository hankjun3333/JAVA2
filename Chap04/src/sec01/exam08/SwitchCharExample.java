package sec01.exam08;

public class SwitchCharExample {

	public static void main(String[] args) {
		char grade = 'c';
		
		switch(grade) {
			case 'A':	
			case 'b':
				System.out.println("우수 회원입니다");
			case 'B':
			case 'c':
				System.out.println("안 우수 회원임니다 " + grade + " 이니까요");
		
		
		
		}
		
		
		
		
	}

}
